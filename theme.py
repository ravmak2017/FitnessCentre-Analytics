def _img_to_base64(path: Path) -> str:
    with open(path, "rb") as f:
        return base64.b64encode(f.read()).decode()


def show_logo(width: int = 140) -> None:
    target = LOGO_PATH if LOGO_PATH.exists() else (LOGO_FALLBACK if LOGO_FALLBACK.exists() else None)
    if target:
        data = _img_to_base64(target)
        st.markdown(
            f'<div class="brand-logo-wrap"><img src="data:image/png;base64,{data}" '
            f'width="{width}" style="border-radius: 50%;"/></div>',
            unsafe_allow_html=True,
        )
    else:
        st.markdown(
            f'<div class="brand-logo-wrap" style="font-size:1.8rem; font-weight:900; color:{TEXT_HI};">ABC</div>',
            unsafe_allow_html=True,
        )


