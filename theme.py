def kpi_tile(value: str, label: str, sub: str = "", actual: str = "",
             delta: str = "", delta_positive: bool = True,
             accent_color: str | None = None) -> None:
    """Premium KPI tile with short value + (optional) exact actual + (optional) delta + (optional) sub.

    value:   short label e.g. '₹13.30L'
    actual:  full-precision e.g. '₹13,30,000'  (rendered smaller, muted)
    delta:   '+18.2% vs prev' rendered green/red below actual
    sub:     extra context line (e.g. '12 mo · all segments')
    """
    border_color = accent_color or ACCENT
    html = (
        f'<div class="brand-kpi" style="border-top-color:{border_color};">'
        f'<div class="brand-kpi-label">{label}</div>'
        f'<div class="brand-kpi-value">{value}</div>'
    )
    if actual:
        html += f'<div class="brand-kpi-actual">{actual}</div>'
    if delta:
        cls = "brand-kpi-delta-up" if delta_positive else "brand-kpi-delta-down"
        html += f'<div class="{cls}">{delta}</div>'
    if sub:
        html += f'<div class="brand-kpi-sub">{sub}</div>'
    html += '</div>'
    st.markdown(html, unsafe_allow_html=True)
