def language_toggle_sidebar() -> None:
    if "lang" not in st.session_state:
        st.session_state.lang = "en"
    st.markdown(
        f'<div style="color:{TEXT_MUTED}; font-size:0.7rem; font-weight:700; '
        f'text-transform:uppercase; letter-spacing:0.12em; text-align:center; margin-bottom:0.4rem;">'
        f'🌐 {t("language")}</div>',
        unsafe_allow_html=True,
    )
    c1, c2 = st.columns(2)
    with c1:
        if st.button(
            "EN", use_container_width=True,
            type="primary" if st.session_state.lang == "en" else "secondary",
            key="lang_btn_en",
        ):
            st.session_state.lang = "en"
            st.rerun()
    with c2:
        if st.button(
            "हिं", use_container_width=True,
            type="primary" if st.session_state.lang == "hi" else "secondary",
            key="lang_btn_hi",
        ):
            st.session_state.lang = "hi"
            st.rerun()


