def t(key: str, **kwargs) -> str:
    """Translate a UI key to current language. Falls back to English if missing."""
    lang = st.session_state.get("lang", "en")
    text = T.get(lang, T["en"]).get(key) or T["en"].get(key, key)
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, IndexError):
            return text
    return text


def get_lang() -> str:
    return st.session_state.get("lang", "en")


# ─── Indian-number formatting helpers ──────────────────────
def fmt_short_inr(v: float) -> str:
    """₹13.30L / ₹1.45cr / ₹85K / ₹342 — compact Indian style."""
    a = abs(v)
    sign = "-" if v < 0 else ""
    if a >= 1e7:
        return f"{sign}₹{a/1e7:.2f}cr"
    if a >= 1e5:
        return f"{sign}₹{a/1e5:.2f}L"
    if a >= 1e3:
        return f"{sign}₹{a/1e3:.1f}K"
    return f"{sign}₹{a:,.0f}"


def fmt_inr(v: float) -> str:
    """Standard Indian comma format ₹13,30,000."""
    a = abs(v)
    sign = "-" if v < 0 else ""
    s = f"{int(round(a)):,}"
    # Convert Western to Indian comma style (last 3, then every 2)
    if len(s.replace(",", "")) > 3:
        x = s.replace(",", "")
        last3 = x[-3:]
        rest = x[:-3]
        # Group rest in pairs from the right
        chunks = []
        while len(rest) > 2:
            chunks.append(rest[-2:])
            rest = rest[:-2]
        if rest:
            chunks.append(rest)
        s = ",".join(reversed(chunks)) + "," + last3
    return f"{sign}₹{s}"


def fmt_pct(v: float, digits: int = 1) -> str:
    return f"{v:.{digits}f}%"


def fmt_chart_label(v: float, show_currency: bool = True) -> str:
    """Compact label for data-label overlays on charts. '1.2L', '85K', '2.3cr'."""
    a = abs(v)
    sign = "-" if v < 0 else ""
    prefix = "₹" if show_currency else ""
    if a >= 1e7:
        return f"{sign}{prefix}{a/1e7:.2f}cr"
    if a >= 1e5:
        return f"{sign}{prefix}{a/1e5:.2f}L"
    if a >= 1e3:
        return f"{sign}{prefix}{a/1e3:.1f}K"
    return f"{sign}{prefix}{a:.0f}"


